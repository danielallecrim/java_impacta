package java_impacta;

public class ExemploLiteraisFloat {
	public static void main(String[] args) {
		
		float num = 10.5f;
		
		double salario = 3523.18;
		
		double tamanhoMolecula = 2.34e-10; // notação científica elevado a 10
		
		System.out.println(salario);
		
		System.out.println(tamanhoMolecula);
		
		
		System.out.println(num);
	}
}
